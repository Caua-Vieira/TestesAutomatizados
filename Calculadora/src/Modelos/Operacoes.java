package Modelos;

public class Operacoes {
	
	public int multiplicacao(int a, int b) {
        return a * b;
    }
	
	public int divisao(int a, int b){
        return a / b;
    }
	
	public int somar(int a, int b) {
        return a + b;
    }
	
	public int subtracao(int a, int b) {
        return a - b;
    }
}
